#include <stdio.h>

// 다차원 배열

int main_04(void) {

	// 1번 방법
	int arr[1][2];
	arr[0][0] = 10;
	arr[0][1] = 20;
	arr[1][0] = 30;
	arr[1][1] = 40;

	// 2번 방법
	int arr_2[2][2] = {
		{10, 20},	// arr_2[0]
		{30, 40}	// arr_2[1]
	};

	// 3차원 배열
	int arr_3[3][3][2] = {
		{
			{1,2},{3,4}
		},

		{
			{3,4},{5,6}
		},

		{
			{5,6},{7,8}
		}

	};
	
	return 0;
}